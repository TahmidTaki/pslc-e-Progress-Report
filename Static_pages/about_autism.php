		<h2 class="text-3xl font-bold text-center py-8">ABOUT AUTISM</h2>
		<p>
			

To understand autism, we must first understand one thing: autism is a hidden disorder. There is no way of telling whether or not a child is autistic just looking at them. A child might look perfectly normal but still suffer from autism. <br><br>

But what exactly is autism? We now know that that autism is not a single disorder. It is not as simple as that. Today medical professionals agree that autism is, in fact, a spectrum of disorders. Hence, it is now more commonly referred to as Autism Spectrum Disorder (ASD). <br><br>

But what do medical professionals mean when they say that autism is actually a spectrum of disorders? This means that no two people with autism will have the same symptoms. In addition, they may experience different combinations of the various symptoms and exhibit varying levels of the severity. <br><br>

But is there an easier way for the layman to understand ASD? Fortunately, there is. First of all, we can start by saying that ASD is a neuro-developmental disorder. This just means that ASD is a disorder that has to do with the development of the brain. <br>	<br>

As a neuro-developmental disorder, ASD is usually is characterised by the following traits:
<ol class="ml-4">
	<li>1. inferior social skills</li>
	<li>2. poor communication skills</li>
	<li>3. inability to process sensory input appropriately</li>
	<li>4. affinity for routines and repetitive bahavior</li>
</ol>
As such, this means that the autistic child is not able to appropriately process what he is experiencing around him. Accordingly, he will also have difficulty in expressing himself and what he feels. <br><br>

In a nutshell, the autistic child is not naughty or necessarily intellectually challenged; he just understands things differently from the way we do.
